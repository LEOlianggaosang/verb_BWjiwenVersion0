void zhuodu_Read(void)
{
	static UCHAR_XDATA sampleCount2 = 0;
	static USHORT_XDATA adValueSum2 = 0;
	Set_Led(IR_LED, MD_FALSE);//打开LED,与三星板的口不同
	singleSample(TURBIDITY,&adValueSum2);
	sampleCount2 ++;

	if (sampleCount2 >= 16)
	{
		sampleCount2 = 0;
		adValueSum2 >>= 6;

		zhuodu_AdValue = (UCHAR)adValueSum2;
		zhuodudianya  = zhuodu[zhuodu_AdValue];//当前浊度AD转换成电压
		adValueSum2 = 0;
	}
}

